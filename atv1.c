#include <stdio.h>
#include <stdlib.h>
#include <time.h> //clock(), CLOCKS_PER_SEC e clock_t
#include <string.h>

//////////////////////////////////////////////////////////
/////////// FUNCAO PARA CALCULO DO FATORIAL  /////////////
//////////////////////////////////////////////////////////

float fatorial(int n){
    float fat = 1.;
    for (int i=1; i<=n; i++){
        fat*=i;
    }
    return fat;
}

//////////////////////////////////////////////////////////
////////////////////////// MAIN //////////////////////////
//////////////////////////////////////////////////////////

int main(){
    clock_t t = clock();
    fatorial(900000);
    t = clock()-t;

    //////////////////////////////////////////////////////////
    ////////////// CALCULO DO TEMPO EM SEGUNDOS //////////////
    //////////////////////////////////////////////////////////

    double temp =  ((double)t)/((CLOCKS_PER_SEC));

    //////////////////////////////////////////////////////
	///////////// ESCREVE NO ARQUIVO DE SAIDA ////////////
	//////////////////////////////////////////////////////

    char str1[50] = "Tempo de execucao: ";
    char str2[50];
    sprintf(str2, "%lf", temp);
    strcat(str1, str2);
    puts(str1);
    FILE *fp;
    fp = fopen("saida.txt", "a");    
    fprintf(fp, "%s ", str1);

    return 0;
}