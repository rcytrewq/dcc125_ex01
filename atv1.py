import time


##########################################################
############## CALCULO DO TEMPO EM SEGUNDOS ##############
##########################################################

def timer (start, end):
	return end-start

##########################################################
########### FUNCAO PARA CALCULO DO FATORIAL  #############
##########################################################

def fatorial (n):
	fat = 1
	for i in range (1,n+1):
		fat*=i
	return fat

##########################################################
########################## MAIN ##########################
##########################################################

def main ():
	start = time.time()
	fatorial(900000)
	end = time.time()

	######################################################
	############# ESCREVE NO ARQUIVO DE SAIDA ############
	######################################################

	print(timer(start, end))
	arq = open("saida.txt", "a")
	arq.write("Tempo de execucao:  "+str(timer(start, end)))

##########################################################
####################### EXECUCAO #########################
##########################################################
main()